package ch01;

public class Binary {

	public static void main(String[] args) {
// 10진수
		int num = 10;
//	2진수	 0B 뒤는 2진수 라는 뜻
		int bNum = 0B1010;
//  8진수 0뒤에 숫자는 8진수 라는 뜻
		int oNum = 012;
//	16진수 	0X 뒤는 16진수 라는 뜻
		int xNum = 0XA;
 
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(xNum);

	}

}